    private void txt_usernameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_usernameActionPerformed
  JTextField nameField = new JTextField(15);
JTextField usernameField = new JTextField(15);
JPasswordField passField = new JPasswordField(15); 

// Dropdown para sa Status
String[] statusOptions = {"Approved", "Pending"};
javax.swing.JComboBox<String> statusCombo = new javax.swing.JComboBox<>(statusOptions);

// Dropdown para sa Role (Admin o User)
String[] roleOptions = {"User", "Admin"};
javax.swing.JComboBox<String> roleCombo = new javax.swing.JComboBox<>(roleOptions);

// 2. Pag-set up sa layout sa Popup (Gihimo nakong 5 rows para sa Role)
javax.swing.JPanel myPanel = new javax.swing.JPanel();
myPanel.setLayout(new java.awt.GridLayout(5, 2, 10, 10)); 

myPanel.add(new javax.swing.JLabel("Name:"));
myPanel.add(nameField);
myPanel.add(new javax.swing.JLabel("Username/Email:"));
myPanel.add(usernameField);
myPanel.add(new javax.swing.JLabel("Password:"));
myPanel.add(passField);
myPanel.add(new javax.swing.JLabel("Status:"));
myPanel.add(statusCombo);
myPanel.add(new javax.swing.JLabel("Account Role:"));
myPanel.add(roleCombo); // Gidugang ang Role dropdown

// 3. I-show ang Popup
int result = JOptionPane.showConfirmDialog(null, myPanel, 
           "Add New User", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

// 4. Inig click sa OK
if (result == JOptionPane.OK_OPTION) {
    String name = nameField.getText().trim();
    String user = usernameField.getText().trim();
    String pass = new String(passField.getPassword());
    String stat = statusCombo.getSelectedItem().toString(); 
    String role = roleCombo.getSelectedItem().toString(); // Kuhaon ang Role
    
    if(name.isEmpty() || user.isEmpty() || pass.isEmpty()){
        JOptionPane.showMessageDialog(null, "Please fill up all fields!");
    } else {
        config con = new config();
        
        // 5. SQL Query: Gidugangan og 'role' nga column
        // Siguruha nga ang imong table naay column named 'role'
        String sql = "INSERT INTO tbl_accounts (what_should_we_call_you, email, password, status, role, date_reg) "
                   + "VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP)";
        
        // Pag-insert sa Database (5 na ka parameters)
        con.addRecord(sql, name, user, pass, stat, role);
        
        // Refresh sa Table Display
        displayData(); 
        JOptionPane.showMessageDialog(null, "User Added Successfully!");
    }
    }//GEN-LAST:event_txt_usernameActionPerformed
    }
    private void txt_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_idActionPerformed
      // 1. I-check kung naay gi-select nga row sa table
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please Choose Table!");
        return;
    }

    // 2. Kuhaon ang data gikan sa table columns
    String id = jTable1.getValueAt(row, 0).toString();
    String currentName = jTable1.getValueAt(row, 1).toString();
    String currentUsername = jTable1.getValueAt(row, 2).toString();
    String currentStatus = jTable1.getValueAt(row, 3).toString();

    // 3. Paghimo sa mga input fields ug i-set ang ilang sulod
    JTextField nameField = new JTextField(currentName, 15);
    JTextField usernameField = new JTextField(currentUsername, 15);
    JTextField statusField = new JTextField(currentStatus, 15);

    // 4. I-set up ang layout sa popup
    javax.swing.JPanel myPanel = new javax.swing.JPanel();
    myPanel.setLayout(new java.awt.GridLayout(3, 2, 5, 5));
    myPanel.add(new javax.swing.JLabel("Edit Name:"));
    myPanel.add(nameField);
    myPanel.add(new javax.swing.JLabel("Edit Username/Email:"));
    myPanel.add(usernameField);
    myPanel.add(new javax.swing.JLabel("Edit Status:"));
    myPanel.add(statusField);

    // 5. I-pakita ang Popup
    int result = JOptionPane.showConfirmDialog(null, myPanel, 
               "Update User ID: " + id, JOptionPane.OK_CANCEL_OPTION);

    // 6. Inig click sa OK, i-update ang Database
    if (result == JOptionPane.OK_OPTION) {
        config con = new config();
        String sql = "UPDATE tbl_accounts SET what_should_we_call_you = ?, email = ?, status = ? WHERE u_id = ?";
        
        con.updateRecord(sql, nameField.getText(), usernameField.getText(), statusField.getText(), id);
        
        displayData(); // I-refresh ang table
        JOptionPane.showMessageDialog(null, "Successfully Updated!");
    }

    }//GEN-LAST:event_txt_idActionPerformed

