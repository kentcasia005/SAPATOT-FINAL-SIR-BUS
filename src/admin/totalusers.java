    private void txt_status1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_status1ActionPerformed
       int row = jTable1.getSelectedRow();
    
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a user from the table to clear!");
        return;
    }

    String userId = jTable1.getValueAt(row, 0).toString(); // Kuhaon ang ID sa Column 0
    
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to clear/archive User ID: " + userId + "?", 
        "Clear Confirmation", 
        JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        config con = new config();
        // Usbon ang status para matago sa main table
       String sql = "UPDATE tbl_accounts SET status = 'Archived' WHERE u_id = ?";
// GI-TAWAG NIMO DIRI:
con.updateRecord(sql, userId);
        
        displayData(); // I-refresh ang table aron mawala ang archived user
        JOptionPane.showMessageDialog(this, "User cleared and moved to Archive!");
    }

    }//GEN-LAST:event_txt_status1ActionPerformed

    private void txt_status2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_status2ActionPerformed
        int row = jTable1.getSelectedRow();
    
    // 1. Check kung naay napili nga user sa table
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Palihog pagpili una og user sa table nga gusto nimo i-restore!");
        return;
    }

    // 2. Kuhaon ang u_id sa napili nga row (Column 0)
    String userId = jTable1.getValueAt(row, 0).toString(); 
    String userName = jTable1.getValueAt(row, 1).toString(); // what_should_we_call_you
    
    // 3. Confirmation Dialog sa English/Bisaya
    int confirm = JOptionPane.showConfirmDialog(this, 
        "You Want to Restore this User " + userName + " (ID: " + userId + ") Back to Active list?", 
        "Restore Confirmation", 
        JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        config con = new config();
        
        // 4. I-update ang status gikan sa 'Archived' balik sa 'Approved'
        String sql = "UPDATE tbl_accounts SET status = 'Approved' WHERE u_id = ?";
        con.updateRecord(sql, userId);
        
        // 5. I-refresh ang Archive Table para mawala na ang gi-restore nga user
        String refreshSql = "SELECT u_id, what_should_we_call_you, email, status, date_reg FROM tbl_accounts WHERE status = 'Archived'";
        con.displayData(refreshSql, jTable1); 
        
        JOptionPane.showMessageDialog(this, "User successfully restored!");
    }
    }//GEN-LAST:event_txt_status2ActionPerformed

    
