    public void displayData() {
    config con = new config();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // I-clear ang table sa dili pa i-load

    try {
        // Ang Query: Approved ra ang ipakita
        java.sql.ResultSet rs = con.getData("SELECT * FROM tbl_accounts WHERE status = 'Approved'");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("u_id"), // Siguruha nga husto ang column name sa imong DB
                rs.getString("what_should_we_call_you"), 
                rs.getString("email"), 
                rs.getString("status"), // O bisan unsang column nga gusto nimo ipakita
                rs.getString("date_reg")
            });
        }
    } catch (java.sql.SQLException ex) {
        System.out.println("Error Loading Table: " + ex.getMessage());
    }
}
