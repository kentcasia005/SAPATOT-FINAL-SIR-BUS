    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
   int row = jTable1.getSelectedRow();
    
    // 1. Check if a row is selected
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a user from the table to delete!");
        return;
    }

    // 2. Get the Unique ID (Column 0)
    String userId = jTable1.getValueAt(row, 0).toString(); 
    
    // 3. Confirm Deletion in English
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete User ID: " + userId + "?", 
        "Delete Confirmation", 
        JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        config con = new config();
        
        // 4. SQL Execution
        // Ensure 'u_id' matches the column name in your SQLite database
        String sql = "DELETE FROM tbl_accounts WHERE u_id = ?";
        con.deleteRecord(sql, userId);
        
        // 5. Refresh and Feedback
        displayData(); // Reloads the table to show updated list
        JOptionPane.showMessageDialog(this, "User deleted successfully!");
    }


    }//GEN-LAST:event_jButton3ActionPerformed

