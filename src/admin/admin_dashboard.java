    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
config con = new config();
    int rowCount = jTable2.getRowCount();
    boolean itemSelected = false;

    // 1. Mangutana kung sigurado ba i-approve ang mga gipili
    int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to approve the selected accounts?", 
            "Approve Confirmation", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        // 2. I-loop ang tibuok table
        for (int i = 0; i < rowCount; i++) {
            // I-check ang value sa Column 6 (Select checkbox)
            Object isCheckedObj = jTable2.getValueAt(i, 6);
            Boolean isChecked = (isCheckedObj instanceof Boolean) ? (Boolean) isCheckedObj : false;

            if (isChecked != null && isChecked) {
                // 3. Kuhaon ang email isip primary identifier (Column 3)
                String email = jTable2.getValueAt(i, 3).toString();
                
                // 4. SQL Update statement
                String sql = "UPDATE tbl_accounts SET status = 'Approved' WHERE email = ?";
                
                // 5. I-execute ang update gamit ang imong config class
                con.updateRecord(sql, email); 
                itemSelected = true;
            }
        }

        if (itemSelected) {
            // 6. Refresh ang table data para mawala ang mga na-approve na (kay Pending ra man ang gina-display)
            updateTableData(); 
            JOptionPane.showMessageDialog(this, "Selected accounts have been approved!");
        } else {
            JOptionPane.showMessageDialog(this, "Please select at least one account to approve.");
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed
    }
