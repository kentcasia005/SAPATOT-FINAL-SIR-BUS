public void updateTableData() {
    config con = new config();
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0); 

    try {
        java.sql.ResultSet rs = con.getData("SELECT * FROM tbl_accounts WHERE status = 'Pending'");
        
        // Kung walay pending, ayaw pag-popup. I-print lang sa console para sa debug.
        if (!rs.isBeforeFirst()) { 
            System.out.println("No pending accounts found."); 
            this.setVisible(true); // Siguruha nga makita gihapon ang dashboard
            return; 
        }

        // Kung naay pending, i-load sa table
        int counter = 1;
        while(rs.next()){
            model.addRow(new Object[]{
                counter++, 
                rs.getString("what_should_we_call_you"), 
                "Casia", 
                rs.getString("email"), 
                rs.getString("date_reg"),
                rs.getString("status"),
                false 
            });
        }
        this.setVisible(true); // I-show ang dashboard kung naay data
    } catch (SQLException ex) {
        System.out.println("Error Loading Table: " + ex.getMessage());
    }
}

