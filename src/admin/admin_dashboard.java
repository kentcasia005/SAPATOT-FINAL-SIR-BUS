    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        config con = new config();
    int rowCount = jTable2.getRowCount();
    boolean itemSelected = false;

    // 1. Mangutana sa Admin kung sigurado ba gyud siya mofuta (Confirmation)
    int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete the selected accounts?", 
            "Delete Confirmation", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        for (int i = 0; i < rowCount; i++) {
            // I-check kung naka-check ba ang checkbox sa ika-pito nga column (index 6)
            Boolean isChecked = (Boolean) jTable2.getValueAt(i, 6);
            
            if (isChecked != null && isChecked) {
                // Kuhaon ang email (index 3) ising primary key para sa pag-delete
                String email = jTable2.getValueAt(i, 3).toString();
                
                // Saktong SQL para sa pag-delete
                String sql = "DELETE FROM tbl_accounts WHERE email = ?";
                
                con.deleteRecord(sql, email); // Siguruha nga naay deleteRecord method sa imong config
                itemSelected = true;
            }
        }

        if (itemSelected) {
            updateTableData(); // I-refresh ang table aron mawa ang gi-delete
            JOptionPane.showMessageDialog(this, "Selected accounts have been deleted!");
        } else {
            JOptionPane.showMessageDialog(this, "Please select at least one account to delete.");
        }
    }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
       DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    jTable2.setRowSorter(sorter);
    
    // Kuhaon ang text nga gi-type
    String query = txt_search.getText();
    
    // I-filter ang table base sa gi-type (Case Insensitive)
    // Ang "(?i)" nagpasabot nga dili sensitive sa Big or Small letters
    sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
    }//GEN-LAST:event_txt_searchKeyReleased

