    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // UPDATE QTY functionality
        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select an item to update quantity");
                return;
            }
            
            // Get current item details
            String itemName = jTable1.getValueAt(selectedRow, 0).toString();
            String currentQtyStr = javax.swing.JOptionPane.showInputDialog(this, 
                "Enter new quantity for " + itemName + ":", 
                "Update Quantity", 
                javax.swing.JOptionPane.PLAIN_MESSAGE);
            
            if (currentQtyStr != null && !currentQtyStr.trim().isEmpty()) {
                try {
                    int newQty = Integer.parseInt(currentQtyStr.trim());
                    if (newQty > 0) {
                        // Update quantity in CartStore
                        CartStore.updateQuantity(itemName, newQty);
                        
                        // Refresh table
                        CartStore.fillTable(jTable1);
                        
                        // Update totals
                        updateTotals();
                        
                        javax.swing.JOptionPane.showMessageDialog(this, 
                            "Quantity updated successfully!", 
                            "Success", 
                            javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        javax.swing.JOptionPane.showMessageDialog(this, 
                            "Quantity must be greater than 0", 
                            "Error", 
                            javax.swing.JOptionPane.ERROR_MESSAGE);
                    }
                } catch (NumberFormatException e) {
                    javax.swing.JOptionPane.showMessageDialog(this, 
                        "Please enter a valid number", 
                        "Error", 
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error updating quantity: " + e.getMessage(), 
                "Error", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        // REMOVE functionality
        try {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select an item to remove");
                return;
            }
            
            // Get item details for confirmation
            String itemName = jTable1.getValueAt(selectedRow, 0).toString();
            String itemPrice = jTable1.getValueAt(selectedRow, 1).toString();
            String itemQty = jTable1.getValueAt(selectedRow, 2).toString();
            
            // Confirm removal
            int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to remove:\n" + 
                itemName + " (Qty: " + itemQty + ")\n" +
                "Price: " + itemPrice,
                "Remove Item",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE);
            
            if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                // Remove item from CartStore
                CartStore.removeItem(itemName);
                
                // Refresh table
                CartStore.fillTable(jTable1);
                
                // Update totals
                updateTotals();
                
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Item removed successfully!", 
                    "Success", 
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error removing item: " + e.getMessage(), 
                "Error", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

