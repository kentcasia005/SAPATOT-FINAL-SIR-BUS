    private void jLabel22MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel22MouseClicked
        // CHECKOUT FUNCTIONALITY
        System.out.println("CHECKOUT button clicked!");
        System.out.println("Cart items: " + CartStore.items.size());
        
        try {
            // Get cart data
            double subtotal = CartStore.getSubtotal();
            double total = CartStore.getTotal();
            
            System.out.println("Subtotal: " + subtotal);
            System.out.println("Total: " + total);
            
            // Pass cart data to checkout page
            checkout checkoutPage = new checkout(this.id); // Pass user ID
            checkoutPage.setOrderData(subtotal, total);
            checkoutPage.setVisible(true);
            checkoutPage.setLocationRelativeTo(null);
            this.dispose(); // Close cart page
        } catch (Exception e) {
            System.err.println("Checkout error: " + e.getMessage());
            javax.swing.JOptionPane.showMessageDialog(this, "Error proceeding to checkout: " + e.getMessage());
        }
    }//GEN-LAST:event_jLabel22MouseClicked

